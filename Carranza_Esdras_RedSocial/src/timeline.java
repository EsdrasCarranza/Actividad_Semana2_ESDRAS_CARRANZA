    private void publicarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_publicarMouseClicked
         
                 String contenido = twet.getText();
                if (contenido.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "El tweet no puede estar vac√≠o.");
                } else {
                    UsuarioInfo usuarioActual = user_actual.getUsuarioActual();
                    usuarioActual.mandarTweet(contenido);
                    twet.setText(""); // Limpiar el campo de texto
                    mostrarTimeline();
                }
    }//GEN-LAST:event_publicarMouseClicked

