private void mostrarTimeline() {
        UsuarioInfo usuarioActual = user_actual.getUsuarioActual();
        String[] timeline = usuarioActual.obtenerTimeline();
        xd.setText("");
        for (String tweet : timeline) {
            xd.append(tweet + "\n\n");
        }
    }
