    private void INICIO_SESIONMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_INICIO_SESIONMouseClicked

        String usuari = USER.getText().trim();
        String contra = CONTRA.getText().trim();
        boolean encontrado= false;
        for (int i = 0; i < UsuarioInfo.getContador(); i++) {
        UsuarioInfo cuenta = UsuarioInfo.getCuenta(i);
        if (cuenta.getusuario().equals(usuari) && cuenta.getcontrase침a().equals(contra)) {
            encontrado = true;
            user_actual.setUsuarioActual(cuenta);
            break;
        }
    }

        if (encontrado) {
           JOptionPane.showMessageDialog(null, "Inicio de sesi칩n exitoso");
           menu_principal menu = new menu_principal();
           menu.setVisible(true);
           this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Usuario o Contrase침a erronea o cuenta inexistente");
        }
    }//GEN-LAST:event_INICIO_SESIONMouseClicked

